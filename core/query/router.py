async def test_router():
    from qdrant_client import AsyncQdrantClient
    qdrant = AsyncQdrantClient(url="http://localhost:6333")
    
    router = QueryRouter(
        qdrant_client=qdrant,
        slm_model="llama3.1:8b-instruct-q4_K_M",
        # LLM Config (Local Ollama)
        llm_base_url="http://localhost:11434/v1",
        llm_api_key="ollama",
        # Embedder Config (Cloud OpenAI)
        embedding_model="text-embedding-3-small",
        embed_base_url="https://api.openai.com/v1",
        embed_api_key="sk-proj-..."
    )
    
    queries = [
        "What is the table next to the Reactor section?",
        "Give me a summary of the entire document",
        "Find the specifications for Valve V-101"
    ]
    
    for q in queries:
        print(f"\nQuery: {q}")
        try:
            plan = await router.route(q, tenant_id="default")
            print(json.dumps(plan.model_dump(), indent=2))
        except Exception as e:
            print(f"Error: {e}")

if __name__ == "__main__":
    asyncio.run(test_router())